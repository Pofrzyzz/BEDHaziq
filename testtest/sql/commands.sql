CREATE TABLE Users (
    Email VARCHAR(255),
    PASSWORD VARCHAR(255),
    Postalcode INT(6),
    Streetname VARCHAR(255),
    Blockno INT(4),
    Unitno INT,
    Phoneno int(8),
    Name VARCHAR(255)   
)

CREATE TABLE Account (
    Name VARCHAR(255),
    Id INT,
    Details VARCHAR(255),
    country VARCHAR(255)
)
 
   